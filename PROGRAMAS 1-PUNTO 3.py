#  Convertir una temperatura de grados Celsius C a Fahrenheit F . F ́ormula:F = 95 C + 32. Haz un progrma en thony

celsius = float(input("Digite la temperatura que corresponde en grados Celsius: "))

# Convertirmos la temperatura a grados Fahrenheit utilizando la fórmula F = (9/5)*C + 32 de la siguiente manera
fahrenheit = (9/5) * celsius + 32


# Obtenenos el resultado
print("La temperatura corresponde a ", celsius, " los grados Celsius son equivalentes a", fahrenheit, "grados Fahrenheit.")